% 时域连续波形图
t = 0:0.001:3; % 时间范围为0到3秒，采样间隔为0.001秒
x = cos(2*pi*t) + 2*sin(4*pi*t) + 2*cos(6*pi*t) + cos(8*pi*t); % 计算连续信号x(t)
figure; % 创建一个新的图形窗口
subplot(2,2,1); % 将图分为2行2列，当前绘制第1个子图
plot(t, x); % 绘制时域波形图
xlabel('时间 (s)'); % 设置横坐标标签
ylabel('幅值'); % 设置纵坐标标签
title('时域连续波形图'); % 设置图形标题
% 采样得到周期序列
Ts = 1/12; % 采样周期为1/12秒（fs=12Hz）
n = 0:2*12-1; % 采样点数为2个周期
xn = cos(2*pi*n*Ts) + 2*sin(4*pi*n*Ts) + 2*cos(6*pi*n*Ts) + cos(8*pi*n*Ts); % 采样得到的周期序列
% 时域波形图（2个周期）
subplot(2,2,2); % 将图分为2行2列，当前绘制第2个子图
stem(n, xn); % 绘制离散时域波形图
xlabel('采样点'); % 设置横坐标标签
ylabel('幅值'); % 设置纵坐标标签
title('时域离散波形图（2个周期）'); % 设置图形标题
% 离散幅度频谱图
Xk = fft(xn); % 对周期序列进行快速傅里叶变换（FFT）
f = (0:length(Xk)-1)*(12/length(Xk)); % 计算频率坐标
subplot(2,2,3); % 将图分为2行2列，当前绘制第3个子图
stem(f, abs(Xk)); % 绘制离散幅度频谱图
xlabel('频率 (Hz)'); % 设置横坐标标签
ylabel('幅度'); % 设置纵坐标标签
title('离散幅度频谱图'); % 设置图形标题
% 离散相位频谱图
subplot(2,2,4); % 将图分为2行2列，当前绘制第4个子图
stem(f, angle(Xk)); % 绘制离散相位频谱图
xlabel('频率 (Hz)'); % 设置横坐标标签
ylabel('相位 (弧度)'); % 设置纵坐标标签
title('离散相位频谱图'); % 设置图形标题