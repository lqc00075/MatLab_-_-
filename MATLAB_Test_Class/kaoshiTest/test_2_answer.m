% 序列值和波形图
n1 = 0:47; % 序列范围为n=0~47
n2 = 0:95; % 序列范围为n=0~95
x1 = cos(n1*pi/4) + cos(n1*pi/8) + cos(pi*n1/12) + cos(pi*n1/24); % 计算n1范围内序列值和
x2 = cos(n2*pi/4) + cos(n2*pi/8) + cos(pi*n2/12) + cos(pi*n2/24); % 计算n2范围内序列值和
figure; % 创建一个新的图形窗口
subplot(2,2,1); % 将图分为2行2列，当前绘制第1个子图
stem(n1, x1, 'filled'); % 绘制x1(n)的波形图，实心点表示
xlabel('n'); % 设置横坐标标签
ylabel('幅值'); % 设置纵坐标标签
title('x1(n)的波形图 (n=0~47)'); % 设置图形标题
subplot(2,2,2); % 将图分为2行2列，当前绘制第2个子图
stem(n2, x2, 'filled'); % 绘制x2(n)的波形图，实心点表示
xlabel('n'); % 设置横坐标标签
ylabel('幅值'); % 设置纵坐标标签
title('x2(n)的波形图 (n=0~95)'); % 设置图形标题
% DFT及离散谱图绘制
X1 = fft(x1, 48); % 对x1(n)进行48点DFT
X2 = fft(x2, 96); % 对x2(n)进行96点DFT
f1 = (0:length(X1)-1); % 频率坐标
f2 = (0:length(X2)-1); % 频率坐标
subplot(2,2,3); % 将图分为2行2列，当前绘制第3个子图
stem(f1, abs(X1), 'filled'); % 绘制离散幅度谱图，实心点表示
xlabel('频率 (Hz)'); % 设置横坐标标签
ylabel('幅度'); % 设置纵坐标标签
title('48点离散幅度谱'); % 设置图形标题
subplot(2,2,4); % 将图分为2行2列，当前绘制第4个子图
stem(f2, abs(X2), 'filled'); % 绘制离散幅度谱图，实心点表示
xlabel('频率 (Hz)'); % 设置横坐标标签
ylabel('幅度'); % 设置纵坐标标签
title('96点离散幅度谱'); % 设置图形标题
