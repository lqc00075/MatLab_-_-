% 零状态响应
h = [1, 2, 3, 2, 1]; % 系统的冲激响应
x = [1, 1, 1, 1, 1, 1]; % 输入信号
y = conv(x, h); % 系统的零状态响应
% 输入信号一个周期的幅度谱图
X = fft(x); % 输入信号的傅里叶变换
f = (0:length(X)-1)*(1/length(X)); % 频率坐标
figure; % 创建一个新的图形窗口
subplot(2, 2, 1); % 将图分为2行2列，当前绘制第1个子图
plot(f, abs(X)); % 绘制输入信号的幅度谱图
xlabel('频率 (Hz)'); % 设置横坐标标签
ylabel('幅度'); % 设置纵坐标标签
title('输入信号的幅度谱图'); % 设置图形标题
% 系统的零极点分布图
subplot(2, 2, 2); 
zplane(h,1); % 绘制系统的零极点分布图
title('系统的零极点分布图'); % 设置图形标题
% 系统在区间[0,π)内的幅频特性曲线和相频特性曲线
[H, w] = freqz(h, 1); % 计算系统的频率响应
subplot(2, 2, 3); % 将图分为2行2列，当前绘制第3个子图
plot(w, abs(H)); % 绘制幅频特性曲线
xlabel('频率 (rad/sample)'); % 设置横坐标标签
ylabel('幅度'); % 设置纵坐标标签
title('幅频特性曲线'); % 设置图形标题
subplot(2, 2, 4); % 将图分为2行2列，当前绘制第4个子图
plot(w, angle(H)); % 绘制相频特性曲线
xlabel('频率 (rad/sample)'); % 设置横坐标标签
ylabel('相位'); % 设置纵坐标标签
title('相频特性曲线'); % 设置图形标题